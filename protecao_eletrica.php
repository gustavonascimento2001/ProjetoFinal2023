		<p>A proteção elétrica é um conjunto de medidas e dispositivos utilizados para garantir a segurança das instalações elétricas, prevenir acidentes e proteger equipamentos e pessoas contra falhas e sobrecargas elétricas. Ela desempenha um papel fundamental na preservação da integridade dos sistemas elétricos e na prevenção de danos materiais e lesões pessoais.</p>

		<p>Existem diversas formas de proteção elétrica que podem ser adotadas, dependendo das características da instalação e das necessidades específicas de cada situação. A seguir, estão alguns dos principais dispositivos e técnicas utilizados na proteção elétrica:</p>

		<ol>
			<li>Disjuntores: são dispositivos de proteção que desligam automaticamente o circuito elétrico quando há uma sobrecarga ou curto-circuito. Eles são projetados para interromper o fluxo de corrente elétrica quando os valores limites são excedidos, evitando danos aos equipamentos e reduzindo o risco de incêndios.</li>

			<li>Fusíveis: assim como os disjuntores, os fusíveis são dispositivos de proteção contra sobrecorrente. Eles contêm um elemento fusível que se rompe quando a corrente elétrica excede o valor nominal, interrompendo o circuito. Os fusíveis precisam ser substituídos após sua atuação, enquanto os disjuntores podem ser rearmados.</li>

			<li>DPS (Dispositivo de Proteção contra Surtos): os surtos elétricos, causados por descargas atmosféricas, manobras de chaveamento ou falhas no sistema elétrico, podem danificar equipamentos sensíveis e provocar incêndios. Os DPS são dispositivos que limitam a tensão aplicada aos equipamentos, desviando os surtos para o aterramento.</li>

			<li>Aterramento: o aterramento adequado é essencial para garantir a segurança das instalações elétricas. Ele consiste na conexão de condutores metálicos com a terra, criando um caminho de baixa resistência para a corrente elétrica em caso de falhas, descargas atmosféricas ou sobrecargas. O aterramento contribui para a dissipação de energia e evita o acúmulo de tensões perigosas.</li>

			<li>Isolamento adequado: a utilização de materiais isolantes de qualidade é crucial para garantir a proteção elétrica. Cabos, fios e componentes elétricos devem ser isolados de forma adequada para evitar contato acidental com partes energizadas e prevenir curtos-circuitos.</li>

			<li>Proteção diferencial-residual (DR): esse dispositivo é responsável por desligar o circuito elétrico quando há uma corrente de fuga para o terra, protegendo contra choques elétricos. Ele é especialmente importante em instalações residenciais, onde a segurança das pessoas é uma prioridade.</li>
		</ol>

